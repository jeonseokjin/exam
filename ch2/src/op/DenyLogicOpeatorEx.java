package op;
//논리 연산자 : true, false
//논리 부정 연산자 : !(not) 반대로 만들어줌. true => false , false => true // boolean 에서만 사용 가능.
public class DenyLogicOpeatorEx {

	public static void main(String[] args) {
		boolean flag = true;
		System.out.println("flag="+flag);
		
		
		flag=!flag;
		System.out.println("flag="+flag);
		
		
	}

}
