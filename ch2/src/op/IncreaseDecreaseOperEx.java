package op;
//증감연산자(++,--)
public class IncreaseDecreaseOperEx {

	public static void main(String[] args) {
		int i =10;
		++i; // i=i+1;
		System.out.println("i="+i);
		i++; // i=i+1;
		System.out.println("i="+i);
		
		--i; // i=i-1;
		System.out.println("i="+i);
		i--; // i=i-1;
		System.out.println("i="+i);

	}

}
