package basic;
//정수형 : 1. byte 2. short

public class ShortVariable {

	public static void main(String[] args) {
		// short : 2byte 사용 2의 16승
		// -32768 ~32767
		byte b1=12;
		short s1= 200;
		
		//+ : 산술연산
		
//		short result=b1 + s1;
		
		System.out.println("s1="+s1);//+: 연결의 의미. 변수 앞, 뒤쪽으로 문자열이 존재하는 경우)
		System.out.println(b1+s1);//+ : 산술연산

	}

}
