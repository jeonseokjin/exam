package shop;

public class User {
	private String name;  //绊按疙
	private PayType payType;					//搬力规过
	public User(String name, PayType payType) {
		super();
		this.name = name;
		this.payType = payType;
	}
	public String getName() {
		return name;
	}
	
	public PayType getPayType() {
		return payType;
	}
	
	
	
	
}
